function processBooleanValue(boolValue) {
  if (boolValue === true) {
    console.log('The boolean value is true.');
  } else {
    console.log('The boolean value is false.');
  }
}

// Call the function with a boolean value
processBooleanValue(true); // Output: The boolean value is true.
processBooleanValue(false); // Output: The boolean value is false.
